package jetbrains_academy.java.basicsyntax;

public class AutoCastPrim {
    public static void main(String[] args) {

        char u = 'u';
        int t = u;

        int q = 23;
        long o = q;

        float w = o;
        long p = (long) w;

        double a = 124;
        float b = (float) a;

        byte e = 1;
        char y = (char) e;
    }
}
