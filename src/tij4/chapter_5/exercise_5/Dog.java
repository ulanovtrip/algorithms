package tij4.chapter_5.exercise_5;

public class Dog {
    public void bark() {
        System.out.println("Nothing doing");
    }

    public void bark(int i) {
        System.out.println("barking: " + i);
    }

    public void bark(double i) {
        System.out.println("wooooofing: " + i);
    }

    public void bark(char i) {
        System.out.println("oowf oowf oowfing: " + i);
    }
}
